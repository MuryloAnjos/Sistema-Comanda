
package Model;

public class Cozinha {
    
    private int pddId;
    private String pdtNome;
    private int pdtQtd;
    private String pddObs;
    private int num_mesa;
    private int cmaId;

    public int getPddId() {
        return pddId;
    }

    public void setPddId(int pddId) {
        this.pddId = pddId;
    }

    public String getPdtNome() {
        return pdtNome;
    }

    public void setPdtNome(String pdtNome) {
        this.pdtNome = pdtNome;
    }

    public int getPdtQtd() {
        return pdtQtd;
    }

    public void setPdtQtd(int pdtQtd) {
        this.pdtQtd = pdtQtd;
    }

    public String getPddObs() {
        return pddObs;
    }

    public void setPddObs(String pddObs) {
        this.pddObs = pddObs;
    }

    public int getNum_mesa() {
        return num_mesa;
    }

    public void setNum_mesa(int num_mesa) {
        this.num_mesa = num_mesa;
    }

    public int getCmaId() {
        return cmaId;
    }

    public void setCmaId(int cmaId) {
        this.cmaId = cmaId;
    }
    
    
    
}
